# operadores aritmeticos
print(2 + 2) # suma
print(4 - 2) # resta
print(3 * 7) # multiplicacion
print(15 / 2) # division
print(11 % 4) # modulo
print(11 // 4) # division de piso
print(2 ** 3) # potenciacion

# operador en cadenas
print("hola"+"mundo")#concatenacion
print("hola" * 3)#repeticion
print("hola"+"mundo"*3)#concatenacion y repeticion

# operadores de comparacion
print("Hola" == "hola") # igual que
print("Hola" != "hola") # distinto que
print(3 > 11) # mayor que, monor que
print(11 >= 10) # mayor o igual que
print(10 <= 10) # menor o igual que

# operadores de existencia
print("ola" in  "Hola") # de existencia
print("ola" not in "Hola") # de inexistencia
 
# operadores booleanos
print(True or False) #  or verifica que se cumpla solo una de las dos condiciones
print(True and False) # and verifeca que se cumplan las dos condiciones

# operadores de asignacion
x = 1 # operador de asignacion estandar
x += 2 # operador de asignacion suma
x *= 3 # operador de asignacion multiplicacion
x %= 4 # operador de asignacion modulo
print(x)

x = 6 * 5 + 8 / 2 ** 4
print(x)
